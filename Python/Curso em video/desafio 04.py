print('Vamos descobrir os grupos que os caracteres digitados a seguir faz parte! ')
n = input('Digite Algo! ')
print('É um numero? ', n.isnumeric())
print('É um decimal? ', n.isdecimal())
print('É um espaço? ',n.isspace())
print('É alfabetico? ', n.isalpha())
print('É um alfanumero? ', n.isalnum())
print('Esta em maiscula? ', n.isupper())
print('Esta em minuscula? ', n.islower())
print('Esta capitalizada? ', n.istitle())






